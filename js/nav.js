function openNav() {
    var bars = document.querySelectorAll('.bar');
    bars.forEach(function(bar, index) {
      bar.style.opacity = '1';
    });
  }
  
  function closeNav() {
    var bars = document.querySelectorAll('.bar');
    bars.forEach(function(bar, index) {
      bar.style.opacity = '1';
    });
  }